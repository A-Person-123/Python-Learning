## This is a madlibs game
adjective1 = input("Enter an adjective: ")
noun1 = input("Enter a noun: ")
verb1 = input("Enter a verb ending with 'ing': ")
verb2 = input("Enter another verb ending with 'ing': ")
adjective2 = input("Enter another adjective: ")

print(f"Today I ran to a {adjective1} park")
print(f"The park was filled with {noun1}/s")
print(f"The {noun1}/s were {verb1} and {verb2}")
print(f"I felt very {adjective2} while the {noun1}/s were {verb1} and {verb2}")